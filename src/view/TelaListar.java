    private void setTabelasSelected() {
        
        if(listouTabelas) {
            tabelasSelected.clear();
            listouTabelas = false;
        }
        
        for (Tabela tabela : tabelas) {
            
            for (String selected : list.getSelectedValuesList()) {
                
                if (tabela.getNome().equalsIgnoreCase(selected)) {
                    tabelasSelected.add(tabela);
                }
            }
        }   
        
    }


