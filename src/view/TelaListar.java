    private void listarTabelas() {
        list.setModel(new javax.swing.AbstractListModel<String>() {

            public int getSize() {
                return tabelas.size();
            }

            public String getElementAt(int i) {
                return tabelas.get(i).getNome();
            }
        });
    }
    

